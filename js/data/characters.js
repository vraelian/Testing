/**
 * Returns the CSS inline style string for the specified portrait ID.
 * @param {string} portraitId - The key from PortraitRegistry (e.g., 'Merchants_Guild_2')
 * @returns {string} The background-position CSS rule
 */
function getPortraitStyle(portraitId) {
    const coords = PortraitRegistry[portraitId];
    if (!coords) {
        console.warn(`[Portrait System] Missing coordinates for portrait ID: ${portraitId}`);
        return ''; 
    }
    
    // Uses the negative offsets required to map the sprite sheet viewport correctly.
    return `background-image: url('assets/images/characters/characters_sprite.webp'); background-position: -${coords.x}px -${coords.y}px;`;
}

